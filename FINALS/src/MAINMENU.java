    // 2. NEW INNER CLASS: Handles real-time search filtering
    private class SearchDocumentListener implements DocumentListener {
        private JTextField searchField;

        public SearchDocumentListener(JTextField searchField) {
            this.searchField = searchField;
        }

        private void filterCards() {
            String searchText = searchField.getText().toLowerCase();
            
            for (CardPanel card : cardList) {
                String eventName = card.getEventName().toLowerCase();
                
                // Check if the card's event name contains the search text
                boolean visible = eventName.contains(searchText);
                
                card.setVisible(visible);
                
                // This forces the container to re-layout, removing the blank space 
                // where the invisible cards used to be.
                cardsContainer.revalidate();
                cardsContainer.repaint();
            }
        }

        @Override
        public void insertUpdate(DocumentEvent e) {
            filterCards();
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            filterCards();
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            // Not used for simple text fields
        }
    }
